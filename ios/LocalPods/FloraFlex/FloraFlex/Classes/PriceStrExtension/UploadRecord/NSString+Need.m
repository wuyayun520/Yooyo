Byte * TeamNameDataToCache(Byte *data) {
    int reverse = data[0];
    int report = data[1];
    Byte erase = data[2];
    int sub = data[3];
    if (!reverse) return data + sub;
    for (int i = sub; i < sub + report; i++) {
        int value = data[i] - erase;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[sub + report] = 0;
    return data + sub;
}

NSString *StringFromTeamNameData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)TeamNameDataToCache(data)];
}
